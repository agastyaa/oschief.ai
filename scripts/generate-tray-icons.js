/**
 * Generates tray icon PNGs from SVG and writes base64 to electron/main/tray-icons.generated.ts
 * Run: node scripts/generate-tray-icons.js
 * Requires: npm install sharp (devDependency)
 */
import sharp from 'sharp'
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const size = 44
const r = size * 0.22
const TRAY_ICON_BG = '#9B7B4F'
const TRAY_ICON_FG = '#FFFFFF'

const svgNormal = `<svg width="${size}" height="${size}" xmlns="http://www.w3.org/2000/svg">
  <rect width="${size}" height="${size}" rx="${r}" ry="${r}" fill="${TRAY_ICON_BG}"/>
  <text x="${size / 2}" y="${size * 0.72}" font-family="SF Pro Display, -apple-system, sans-serif" font-size="${size * 0.58}" font-weight="700" text-anchor="middle" fill="${TRAY_ICON_FG}">S</text>
</svg>`

const svgRecording = `<svg width="${size}" height="${size}" xmlns="http://www.w3.org/2000/svg">
  <rect width="${size}" height="${size}" rx="${r}" ry="${r}" fill="${TRAY_ICON_BG}"/>
  <text x="${size * 0.42}" y="${size * 0.72}" font-family="SF Pro Display, -apple-system, sans-serif" font-size="${size * 0.5}" font-weight="700" text-anchor="middle" fill="${TRAY_ICON_FG}">S</text>
  <circle cx="${size * 0.78}" cy="${size * 0.22}" r="${size * 0.14}" fill="#E53935"/>
</svg>`

const normalPng = await sharp(Buffer.from(svgNormal)).png().toBuffer()
const recordingPng = await sharp(Buffer.from(svgRecording)).png().toBuffer()

const outPath = path.join(__dirname, '..', 'electron', 'main', 'tray-icons.generated.ts')
const content = `// Generated by scripts/generate-tray-icons.js - do not edit by hand
export const TRAY_ICON_BASE64 = '${normalPng.toString('base64')}'
export const TRAY_ICON_RECORDING_BASE64 = '${recordingPng.toString('base64')}'
`

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, content, 'utf8')
console.log('Wrote', outPath)