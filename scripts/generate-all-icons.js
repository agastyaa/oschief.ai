/**
 * Generates tray, app (Dock), and in-app icons from public/logo-source.png.
 * Source image has three panels: [tray | Mac Dock | in-app].
 * Run: node scripts/generate-all-icons.js
 * Requires: sharp (devDependency). For .icns: macOS with iconutil.
 */
import sharp from 'sharp'
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const sourcePath = path.join(root, 'public', 'logo-source.png')

const W = 1024
const H = 559
const third = Math.floor(W / 3)
const squareSize = Math.min(third, H)
const top = Math.floor((H - squareSize) / 2)

async function main() {
  const source = sharp(sourcePath)
  const meta = await source.metadata()
  if (!meta.width || !meta.height) throw new Error('Invalid source image')

  // ─── 1. Tray icon (left third: monochrome) ─────────────────────────────────
  const leftCrop = await source
    .clone()
    .extract({ left: 0, top, width: squareSize, height: squareSize })
    .resize(44, 44)
    .png()
    .toBuffer()

  const redDot = await sharp({
    create: {
      width: 14,
      height: 14,
      channels: 4,
      background: { r: 229, g: 57, b: 53, alpha: 1 },
    },
  })
    .png()
    .toBuffer()

  const trayRecording = await source
    .clone()
    .extract({ left: 0, top, width: squareSize, height: squareSize })
    .resize(44, 44)
    .composite([{ input: redDot, left: 30, top: 0 }])
    .png()
    .toBuffer()

  const trayOutPath = path.join(root, 'electron', 'main', 'tray-icons.generated.ts')
  const trayContent = `// Generated by scripts/generate-all-icons.js - do not edit by hand
export const TRAY_ICON_BASE64 = '${leftCrop.toString('base64')}'
export const TRAY_ICON_RECORDING_BASE64 = '${trayRecording.toString('base64')}'
`
  fs.mkdirSync(path.dirname(trayOutPath), { recursive: true })
  fs.writeFileSync(trayOutPath, trayContent, 'utf8')
  console.log('Wrote', trayOutPath)

  // ─── 2. In-app icon (right third: flat gradient) ───────────────────────────
  const rightCrop = await source
    .clone()
    .extract({ left: third * 2, top, width: squareSize, height: squareSize })
    .resize(96, 96)
    .png()
    .toBuffer()

  const inAppPath = path.join(root, 'public', 'syag-logo-inapp.png')
  fs.writeFileSync(inAppPath, rightCrop)
  console.log('Wrote', inAppPath)

  const favicon = await source
    .clone()
    .extract({ left: third * 2, top, width: squareSize, height: squareSize })
    .resize(32, 32)
    .png()
    .toBuffer()
  fs.writeFileSync(path.join(root, 'public', 'favicon.png'), favicon)
  console.log('Wrote public/favicon.png')

  // ─── 3. Mac app icon / Dock (middle third) → .icns ───────────────────────────
  const middleCrop = await source
    .clone()
    .extract({ left: third, top, width: squareSize, height: squareSize })
    .resize(1024, 1024)
    .png()
    .toBuffer()

  const resourcesDir = path.join(root, 'electron', 'resources')
  const iconsetDir = path.join(resourcesDir, 'icon.iconset')
  fs.mkdirSync(iconsetDir, { recursive: true })

  const entries = [
    [16, 'icon_16x16.png'],
    [32, 'icon_16x16@2x.png'],
    [32, 'icon_32x32.png'],
    [64, 'icon_32x32@2x.png'],
    [128, 'icon_128x128.png'],
    [256, 'icon_128x128@2x.png'],
    [256, 'icon_256x256.png'],
    [512, 'icon_256x256@2x.png'],
    [512, 'icon_512x512.png'],
    [1024, 'icon_512x512@2x.png'],
  ]
  const middle = sharp(middleCrop)
  for (const [size, name] of entries) {
    const buf = await middle.clone().resize(size, size).png().toBuffer()
    fs.writeFileSync(path.join(iconsetDir, name), buf)
  }

  try {
    const { execSync } = await import('child_process')
    execSync(`iconutil -c icns "${iconsetDir}" -o "${path.join(resourcesDir, 'icon.icns')}"`, {
      stdio: 'inherit',
    })
    console.log('Wrote electron/resources/icon.icns')
  } catch (e) {
    console.warn('iconutil failed (macOS only). icon.icns not updated:', e.message)
  }

  try {
    fs.rmSync(iconsetDir, { recursive: true })
  } catch {}
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
